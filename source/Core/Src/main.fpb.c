uint8_t detect_debugger(void)
{
  uint32_t * FPB_CTRL = (uint32_t *)0xE0002000;
  return *FPB_CTRL&1;
}

int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();

  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, 1);

  while (1)
  {
    if(detect_debugger()) {
      HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, 0);
    } else {
      HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, 1);
    }
    HAL_Delay(200);
  }
}

